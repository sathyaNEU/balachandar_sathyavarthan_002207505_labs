    private void viewReportDetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewReportDetailBtnActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) reportHistoryTable.getModel();
        int index = reportHistoryTable.getSelectedRow();
        if(index<0){
            JOptionPane.showMessageDialog(this, "Select a row to view details");
            return;
        }
        this.local_vital = (Vitals) model.getValueAt(index, 3);
        
        
        temperatureTextField.setText(Float.toString(this.local_vital.getTemperature()));
        bloodPressureTextField.setText(Integer.toString(this.local_vital.getBloodPressure()));
        pulseTextField.setText(Integer.toString(this.local_vital.getPulse()));
        dateTextField.setText(this.local_vital.getDate());
    }//GEN-LAST:event_viewReportDetailBtnActionPerformed

    private void deleteReportDetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteReportDetailBtnActionPerformed
        // TODO add your handling code here:
        int index = reportHistoryTable.getSelectedRow();
        
        if(index < 0){
            JOptionPane.showMessageDialog(this, "Select a row to delete Vitals");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) reportHistoryTable.getModel();
        this.local_vital = (Vitals) model.getValueAt(index, 3);
        this.vitalHistory.deleteVitalsFromVitalsObject(this.local_vital);
        JOptionPane.showMessageDialog(this, "Deleted Successfully");
        
        this.temperatureTextField.setText("");
        this.bloodPressureTextField.setText("");
        this.pulseTextField.setText("");
        this.dateTextField.setText("");
        
        populateTable();
        
    }//GEN-LAST:event_deleteReportDetailBtnActionPerformed


