    private void populateTable() {
        
        DefaultTableModel tableModel = (DefaultTableModel) reportHistoryTable.getModel();
        tableModel.setRowCount(0);
        
        for(Vitals iter : this.vitalHistory.getArrayVitals()){
            Object[] obj = new Object[4];
            obj[0] = String.valueOf(iter.getTemperature());
            obj[1] = String.valueOf(iter.getBloodPressure());
            obj[2] = String.valueOf(iter.getPulse());
            obj[3] = iter;
            System.out.println("populate mothod" + iter);
            
            tableModel.addRow(obj);
                  
        }
    }
